package practice2;

public class ConcatenationAddition {

    public static void main(String[] args) {

        int a = 2;
        int b = 3;

        System.out.println(a+b + " + " + a+b); // because a+b start off the print statement the variables a, b will be added together. Any addition sign that comes after that will be concatenation.
    }


}
